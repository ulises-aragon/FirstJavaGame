package aragon.game.assets.data;

public class Vector2Data {
    private int x;
    private int y;

    public Vector2Data() {}

    public Vector2Data(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
